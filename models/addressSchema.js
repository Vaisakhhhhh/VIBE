const mongoose = require(`mongoose`);

const addressSchema = new mongoose.Schema({
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'user',
        required: true
    },
    name: {
        type: String,
        required: true
    },
    mobileNumber: {
        type: String,
        required: true
    },
    pincode: {
        type: String,
        required: true
    },
    locality: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    landmark: {
        type: String,
        default: ''
    },
    alternatePhone: {
        type: String
    },
    addressType: {
        type: String,
        required: true
    }

}, {timestamps: true});


module.exports = mongoose.model('Address', addressSchema);